   public void actualizar (){
        
       // JOptionPane.showMessageDialog(null,"Act"+persona.toString());
        jtfRut.setText(persona.getRut());
        jtfNombre.setText(persona.getNombre());
        
        if(persona.isGenero())
        {
           jRadioButton2.setSelected(true);
        }   
        else
        {
           jRadioButton1.setSelected(true);
        }
        
        jComboBox1.setSelectedItem(persona.getCarrera());
        jtfFono.setText(persona.getFono());
        jtfFoto.setText(persona.getFoto());
        if(!persona.getFoto().equals(""))
            jLabel5.setIcon(new javax.swing.ImageIcon("fotos\\"+persona.getFoto()));
        //super.setVisible(true);
        //
        
   }
   
