    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
      boolean gen=false;
      
      if(jRadioButton1.isSelected()==true)
      {
         gen=false; //femenino
      }
      if(jRadioButton2.isSelected()==true)
      {
         gen=true;//masculino
      }
        
        
      String rut=jtfRut.getText();
      String nombre=jtfNombre.getText();
      boolean genero=gen;
      String carrera=jComboBox1.getSelectedItem().toString();
      String fono=jtfFono.getText();
      String foto=jtfFoto.getText();
      
        
      if(!rut.equals("")  &&  !nombre.equals("") &&        
              !fono.equals("") && !foto.equals(""))
      {  
        persona=new Persona(rut,nombre,genero,carrera,fono,foto);
        try {
            PersonaDB.agregar(persona);
            limpiar();
        } catch (Exception ex) {
           // Logger.getLogger(IngresoView.class.getName()).log(Level.SEVERE, null, ex);
             JOptionPane.showMessageDialog(null, "Error de acceso a base de datos...");
            
        }
      }
      else
          JOptionPane.showMessageDialog(null, "Registro en blanco...");
        
    }//GEN-LAST:event_btnAgregarActionPerformed


